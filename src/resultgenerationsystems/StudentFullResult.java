     public void mainWork(Connection con,String roll){
        try{
            
            Statement stmt1 = con.createStatement();
            ResultSet rs1 = stmt1.executeQuery("select * from student,board where student.roll_no = board.roll_no and student.roll_no = '"+roll+"'");
            
            while(rs1.next()){
                rollLabel.setText(rs1.getString(2));
                nameLabel.setText(rs1.getString(4));
                fatherLabel.setText(rs1.getString(5));
                motherLabel.setText(rs1.getString(6));
                regLevel.setText(rs1.getString(3));
                dobLabel.setText(rs1.getString(7));
                groupLabel.setText(rs1.getString(8));
                instituteLabel.setText(rs1.getString(13));
                boardLabel.setText(rs1.getString(11));
                typeLabel.setText("Regular");
            }
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("select * from subject where roll_no = '"+roll+"'order by sub_id asc");
            
            Table.setModel(DbUtils.resultSetToTableModel(rs));
            Statement stmt2 = con.createStatement();
            ResultSet rs2 = stmt2.executeQuery("select * from subject where roll_no = '"+roll+"'");
            
            double sum = 0.0,GPA = 0.0;
            int count = 0;
            String grade = "";
            while(rs2.next())
            {
                String marks = rs2.getString(10);
                count++;
                if(marks.equals("A+")){GPA = 5.0; grade = "A+";}
                else if (marks.equals("A")){GPA = 4.0; grade = "A";}
                else if (marks.equals("A-")){GPA = 3.5; grade = "A-";}
                else if (marks.equals("B")){GPA = 3.0;grade = "B";}
                else if (marks.equals("C")){GPA = 2.0;grade = "C";}
                else if (marks.equals("D")){GPA = 1.0;grade = "D";}
                else if(marks.equals("F")){GPA = 0.0;grade = "F"; sum = 0; break;}
                
                sum = GPA + sum;
                
            }
            

            double final_gpa= sum/(double)count;
            
            String grades = calculateGPA(final_gpa);
            
            gpaLabel.setText(new DecimalFormat("#.##").format(final_gpa) +   "   Grade: "+ grades);
        }catch(Exception e){
            System.out.println(e);
        }
        }
    
    

