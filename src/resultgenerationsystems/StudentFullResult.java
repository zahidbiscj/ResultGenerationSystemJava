    public String calculateGPA(double result)
    {
                if(result==5.0){return "A+";}
                else if (result>=4.00&&result<=4.99){return "A";}
                else if (result>=3.50&&result<=3.99){return "A-";}
                else if (result>=3.00&&result<=3.49){return "B";}
                else if (result>=2.00&&result<=2.99){return "C";}
                else if (result>=1.00&&result<=1.99){return "D";}
                else if(result==0.0){return "F";}
                else return "error";
    }
    
